# Metodos de cadenas en python

mensaje:str = "Hola mundo"

# Convertir a mayusculas

mensaje.upper()
print(mensaje.upper())

#Converit a minusculas

print(mensaje.lower())

# Formato de cadenas

var_mensaje:str = "Adios Mundo"

# Imprimir dos o mas variables
print(mensaje,var_mensaje)

#Concatenacion de cadenas
hola_mundo:str = var_mensaje + " " + mensaje
print(hola_mundo)