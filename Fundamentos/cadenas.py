# Manejo de cadenas

mi_cadena:str = "Daniel Garcia"
print(mi_cadena)

# Agregar un salto de linea \n
mi_cadena = 'Daniel \nGarcia'
print(mi_cadena)

# Agregar un tabulador \t
mi_cadena = '\tDaniel Garcia'
print(mi_cadena)


# Sub Cadenas

mensaje:str = "Hola Mundo"
print(mensaje[0:4])
print(mensaje[5:10])